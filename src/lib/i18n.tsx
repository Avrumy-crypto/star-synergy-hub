type I18nContextType = {
  lang: LanguageCode;
  setLang: (language: LanguageCode) => void;
  t: (key: string, vars?: Record<string, string>) => string;
};

const I18nContext = createContext<I18nContextType | null>(null);

const STORAGE_KEY = "site-language";

const getInitialLanguage = (): LanguageCode => {
  if (typeof window === "undefined") return "en";

  const stored = window.localStorage.getItem(STORAGE_KEY) as LanguageCode | null;
  if (stored && (stored === "en" || stored === "tr" || stored === "es")) {
    return stored;
  }

  const browserLang = window.navigator.language.toLowerCase();
  if (browserLang.startsWith("tr")) return "tr";
  if (browserLang.startsWith("es")) return "es";
  return "en";
};

export const I18nProvider = ({ children }: { children: ReactNode }) => {
  const [lang, setLang] = useState<LanguageCode>(getInitialLanguage);

  useEffect(() => {
    window.localStorage.setItem(STORAGE_KEY, lang);
    document.documentElement.lang = lang;
  }, [lang]);

  const value = useMemo<I18nContextType>(
    () => ({
      lang,
      setLang,
      t: (key, vars) => {
        const dictionary = translations[lang];
        const fallbackDictionary = translations.en;
        let text = dictionary[key] ?? fallbackDictionary[key] ?? key;

        if (vars) {
          Object.entries(vars).forEach(([name, val]) => {
            text = text.split(`{{${name}}}`).join(val);
          });
        }

        return text;
      },
    }),
    [lang]
  );

  return <I18nContext.Provider value={value}>{children}</I18nContext.Provider>;
};

export const useI18n = () => {
  const context = useContext(I18nContext);
  if (!context) {
    throw new Error("useI18n must be used within I18nProvider");
  }

  return context;
};
